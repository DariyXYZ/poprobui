def _score_yovayshi(answers: dict) -> dict:
    scores = {s: 0 for s in YOVAYSHI_SCALES}
    for qkey, scale_map in YOVAYSHI_KEY.items():
        ans = answers.get(qkey, "").lower()
        if ans in scale_map:
            scores[scale_map[ans]] += 1
    return scores


# ── O*NET Interest Profiler Short Form (RIASEC) ────────────────────────────
# 60 вопросов, 6 шкал по 10 вопросов
# Ответ: 1(Совсем нет) — 5(Очень нравится)
ONETT_SCALES = {
    "R": list(range(1, 11)),
    "I": list(range(11, 21)),
    "A": list(range(21, 31)),
    "S": list(range(31, 41)),
    "E": list(range(41, 51)),
    "C": list(range(51, 61)),
}
ONETT_LABELS = {
    "R": "R — Реалист",
    "I": "I — Исследователь",
    "A": "A — Художник",
    "S": "S — Социальный",
    "E": "E — Предприниматель",
    "C": "C — Конвенциональный",
}

def _score_onett(answers: dict) -> dict:
    scores = {ONETT_LABELS[code]: 0 for code in ONETT_SCALES}
    for code, q_range in ONETT_SCALES.items():
        label = ONETT_LABELS[code]
        for qnum in q_range:
            val = int(answers.get(f"q{qnum}", 3))
            scores[label] += val
    return scores


# ── Карта интересов Голомштока ─────────────────────────────────────────────
# 174 вопроса, 29 шкал, ответ +2/+1/0/-1/-2
# Шкала вопроса N = ((N-1) mod 29) + 1
GOLOMSHTOK_SCALE_NAMES = {
    1: "Физика", 2: "Математика", 3: "Радиоэлектроника",
    4: "Механика/Техника", 5: "Химия", 6: "Геология/География",
    7: "Биология", 8: "С/Х и лесное хоз-во", 9: "Медицина",
    10: "Педагогика", 11: "Литература", 12: "Журналистика",
    13: "История", 14: "Иностр. языки", 15: "Изо/Черчение",
    16: "Музыка", 17: "Театр/Кино", 18: "Физкультура",
    19: "Военное дело", 20: "Рабочие профессии",
    21: "Бытовой труд/Кулинария", 22: "Мода/Дизайн",
    23: "Экономика", 24: "Право/Юриспруденция",
    25: "Транспорт", 26: "Строительство",
    27: "ИТ/Программирование", 28: "Предпринимательство",
    29: "Психология/Философия",
}

def _score_golomshtok(answers: dict) -> dict:
    scores = {name: 0 for name in GOLOMSHTOK_SCALE_NAMES.values()}
    for i in range(1, 175):
        scale_num = ((i - 1) % 29) + 1
        scale_name = GOLOMSHTOK_SCALE_NAMES[scale_num]
        val = int(answers.get(f"q{i}", 0))
        scores[scale_name] += val
    return scores
