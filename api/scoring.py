"""
Scoring engine for all tests.
test_id values: "ddo" | "yovayshi" | "golomshtok" | "onett"
answers: { "q1": "a", "q2": "b", ... }
Returns: { scale_name: score, ... }
"""


def score_test(test_id: str, answers: dict) -> dict:
    if test_id == "ddo":
        return _score_ddo(answers)
    if test_id == "yovayshi":
        return _score_yovayshi(answers)
    if test_id == "golomshtok":
        return _score_golomshtok(answers)
    if test_id == "onett":
        return _score_onett(answers)
    return {}


# ── ДДО Климова ────────────────────────────────────────────────────────────
# 20 пар, 5 шкал: П Т Ч З Х
DDO_KEY = {
    "П": [("1","a"),("3","b"),("6","a"),("10","a"),("11","a"),("13","b"),("16","a"),("20","a")],
    "Т": [("1","b"),("4","a"),("7","b"),("9","a"),("11","b"),("14","a"),("17","b"),("19","a")],
    "Ч": [("2","a"),("4","b"),("6","b"),("8","a"),("12","a"),("14","b"),("16","b"),("18","a")],
    "З": [("2","b"),("5","a"),("9","b"),("10","b"),("12","b"),("15","a"),("19","b"),("20","b")],
    "Х": [("3","a"),("5","b"),("7","a"),("8","b"),("13","a"),("15","b"),("17","a"),("18","b")],
}
DDO_LABELS = {
    "П": "Природа",
    "Т": "Техника",
    "Ч": "Человек",
    "З": "Знак",
    "Х": "Образ",
}

def _score_ddo(answers: dict) -> dict:
    scores = {label: 0 for label in DDO_LABELS.values()}
    for code, pairs in DDO_KEY.items():
        label = DDO_LABELS[code]
        for (qnum, correct_ans) in pairs:
            if answers.get(f"q{qnum}", "").lower() == correct_ans:
                scores[label] += 1
    return scores


